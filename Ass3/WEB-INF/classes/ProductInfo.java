import java.*;
import java.io.*;
import java.util.*;


public class ProductInfo {

    private String id;
    private String productName;


    public ProductInfo (String id, String productName) {
        this.id = id;
        this.productName = productName;
		
    }

    public String getId() {
        return id;
    }

    public String getproductName() {
        return productName;
    }
	
	

}